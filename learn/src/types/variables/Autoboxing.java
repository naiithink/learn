/**
 * Autoboxing:
 * the automatic conversion that the Java compiler makes
 * between primitive types and their object wrapper classes.
 */

package types.variables;

public class Autoboxing {

    public static void main(String[] args) {
        Character c = 'a';
        Integer n = 10;

        System.out.println(c);
        System.out.println(n);
    }
}
